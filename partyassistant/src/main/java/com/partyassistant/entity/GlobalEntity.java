package com.partyassistant.entity;

public class GlobalEntity {
    private int id;
    private String name;

    public GlobalEntity(){}

    public int getId() {
        return this.id;
    }

    public String getName() {
        return this.name;
    }

    public void setId(int value) {
        this.id = value;
    }

    public void setName(String value) {
        this.name = value;
    }
}
